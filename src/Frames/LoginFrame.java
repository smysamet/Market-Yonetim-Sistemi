    public void doLogin() {
        if (jTextField3.getText().equals("root") && jTextField4.getText().equals("admin")) {
            DatabaseConnector connector = null;
            connector = new MysqlConnector(jTextField2.getText(), jTextField3.getText(), jTextField4.getText());
            connector.connectToDatabase();
            new MainMenuFrame().setVisible(true);
            this.dispose();
            
        }else{
            JOptionPane.showMessageDialog(this, "Username/password is wrong!");
        }

    }


